package library.inventory;

/**
 * Represents different types of habitats suitable for various pets.
 * This enumeration helps categorize pets based on their living environment.
 */
public enum HabitatType {
    
    /**
     * A confined space, typically used for small animals like rodents or birds.
     */
    CAGE,
    
    /**
     * A water-filled enclosure designed for aquatic pets such as fish or turtles.
     */
    AQUARIUM,
    
    /**
     * An enclosure that mimics a dry or humid natural habitat, often used for reptiles or amphibians.
     */
    TERRARIUM,
    
    /**
     * An open environment suitable for pets that require space to move freely, such as dogs or large birds.
     */
    OPEN_SPACE
}

